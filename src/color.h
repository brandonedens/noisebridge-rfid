#ifndef COLOR_H_
#define COLOR_H_

#define COLOR_NORMAL       "\e[0m"
#define COLOR_BOLD_BLACK   "\e[1;30m"
#define COLOR_RED          "\e[31m"
#define COLOR_GREEN        "\e[32m"
#define COLOR_YELLOW       "\e[33m"
#define COLOR_BLUE         "\e[34m"
#define COLOR_MAGENTA      "\e[35m"
#define COLOR_CYAN         "\e[36m"
#define COLOR_BOLD_RED     "\e[91m"
#define COLOR_BOLD_GREEN   "\e[92m"
#define COLOR_BOLD_YELLOW  "\e[93m"
#define COLOR_BOLD_BLUE    "\e[94m"
#define COLOR_BOLD_MAGENTA "\e[95m"
#define COLOR_BOLD_CYAN    "\e[96m"

#define COLOR_BG_RED       "\e[31;7m"
#define COLOR_BG_GREEN     "\e[32;7m"
#define COLOR_BG_YELLOW    "\e[33;7m"
#define COLOR_BG_BLUE      "\e[34;7m"
#define COLOR_BG_MAGENTA   "\e[35;7m"
#define COLOR_BG_CYAN      "\e[36;7m"

#endif /* COLOR_H_ */
